#!/bin/bash

# конструкция while - do - done с вложенным if - then - fi

# объявляю переменную-счетчик
cnt=0

# пока переменная меньше 1000, буду итиреироваться. -le = less or equal
while [ $cnt -le 1000 ]
do
    # Вывод числа и увеличение на 1
    echo $cnt
    cnt=$[$cnt + 1]

    # если число делится на 10, то я вывожу надпись об этом. -eq = equal
    if [ $[$cnt % 10] -eq 0 ]
    then
        echo "Прошли очередные 10 на счётчике!"
    fi
done